
public class 증감연산자대입연산자 {

	public static void main(String[] args) {
		int a= 10;
		
		a++;
		System.out.printf("a++ ==> %d \n" , a);
		
a--;
System.out.printf("a-- ==> %d \n" , a);

a+=5;
System.out.printf("a += 5 ==> %d \n" , a);

a-=5;
System.out.printf("a -= 5 ==> %d \n" , a);

a*=5;
System.out.printf("a *= 5 ==> %d \n" , a);

a/=5;
System.out.printf("a /= 5 ==> %d \n" , a);

	}

}
